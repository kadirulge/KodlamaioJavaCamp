public class FourTransactions {
    public int Add(int sayi1,int sayi2){
        return sayi1 + sayi2;
    }
    public int Sub(int sayi1,int sayi2){
        return sayi1 - sayi2;
    }
    public int Multiply(int sayi1,int sayi2){
        return sayi1 * sayi2;
    }
    public int Divide(int sayi1,int sayi2){
        return sayi1 / sayi2;
    }
}
